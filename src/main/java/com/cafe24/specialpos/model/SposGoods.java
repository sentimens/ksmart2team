package com.cafe24.specialpos.model;

public class SposGoods {
	private String goodsBarcode;
	private String goodsMaker;
	private String goodsCate;
	private String goodsName;
	private String goodsDateTime;
	
	public String getGoodsBarcode() {
		return goodsBarcode;
	}
	public void setGoodsBarcode(String goodsBarcode) {
		this.goodsBarcode = goodsBarcode;
	}
	public String getGoodsMaker() {
		return goodsMaker;
	}
	public void setGoodsMaker(String goodsMaker) {
		this.goodsMaker = goodsMaker;
	}
	public String getGoodsCate() {
		return goodsCate;
	}
	public void setGoodsCate(String goodsCate) {
		this.goodsCate = goodsCate;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public String getGoodsDateTime() {
		return goodsDateTime;
	}
	public void setGoodsDateTime(String goodsDateTime) {
		this.goodsDateTime = goodsDateTime;
	}
	
}
