package com.specialpos.goods.domain;

public class SposGoods {
	private String goods_barcode;
	private String user_id;
	private String goods_maker;
	private String goods_cate;
	private String goods_name;
	private String goods_datetime;
	
	
	public String getGoods_barcode() {
		return goods_barcode;
	}
	public void setGoods_barcode(String goods_barcode) {
		this.goods_barcode = goods_barcode;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getGoods_maker() {
		return goods_maker;
	}
	public void setGoods_maker(String goods_maker) {
		this.goods_maker = goods_maker;
	}
	public String getGoods_cate() {
		return goods_cate;
	}
	public void setGoods_cate(String goods_cate) {
		this.goods_cate = goods_cate;
	}
	public String getGoods_name() {
		return goods_name;
	}
	public void setGoods_name(String goods_name) {
		this.goods_name = goods_name;
	}
	public String getGoods_datetime() {
		return goods_datetime;
	}
	public void setGoods_datetime(String goods_datetime) {
		this.goods_datetime = goods_datetime;
	}
}